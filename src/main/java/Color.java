public final class Color implements Cloneable {
    public float r;
    public float g;
    public float b;
    public float a;

    public Color(float r, float g, float b, float a) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }

    public Color(float r, float g, float b) {
        this(r, g, b, 1.0f);
    }

    @Override
    public Object clone() throws CloneNotSupportedException {
        return super.clone();
    }
}
